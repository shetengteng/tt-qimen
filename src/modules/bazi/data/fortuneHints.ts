/**
 * 取一段大运的一句话模板（v1 旧表）。
 * 找不到时返回兜底文案（不抛错）。
 */
export function getDecadeHint(tenGod: TenGodType, tendency: Tendency): string {
  return DECADE_HINTS[`${tenGod}_${tendency}` as DecadeHintKey]
    || `${tenGod}入运，宜结合用神综合判断。`
}

