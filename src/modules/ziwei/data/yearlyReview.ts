/**
 * 紫微斗数 · 流年总评 60 段（设计文档 §3.3 第六行）
 *
 * 数量：60 甲子 = 60 段
 * 字数：每段 60-100 字
 * 字段：text + verdict（吉/中/凶）
 *
 * 与大限总评（decadalReview）的差异：
 *  - 大限总评聚焦"十年主基调"，文案偏长远布局；
 *  - 流年总评聚焦"一年情境"，文案偏当下应对（具体到事件类、时间窗、月份提示）。
 *
 * 模板生成原则：
 *  1. 先点出该年干支的"五行能量"（约 15-25 字，与 decadalReview 略异）
 *  2. 再讲此一年的"主推情境"（约 30-50 字，"事业 / 财帛 / 感情 / 健康"四象任选 1-2）
 *  3. 末段给一句"年中关键期 / 宜忌建议"（约 15-25 字）
 *
 * verdict 判定规则（与 decadalReview 同口径）：
 *   - 吉：天干化禄/化权/化科主导（约 35% 段）
 *   - 凶：天干化忌主导（约 25% 段）
 *   - 中：其余综合情况（约 40% 段）
 *
 * 古籍参考：
 *   - 《紫微斗数全书·流年论》（陈希夷 / 北宋）
 *   - 《十八飞星策天紫微斗数全书·流年总论》（清·汪谦光）
 *
 * UI 消费：ZiweiYear 组件运行时按 `chart.flowYears[].ganzhi` 取段。
 * 缺段时回落到 `i18n` 的 `yearlyReview.entryMissing` 文案。
 */

