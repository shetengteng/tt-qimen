/**
 * 骨重数值 → 歌诀表查询（浮点容差 0.01，满足"2.2 vs 2.20"的精度风险）
 * 找不到时返回 null，由调用方决定兜底策略。
 */
export function findPoem(totalWeight: number): ChengguPoem | null {
  for (const p of POEMS) {
    if (Math.abs(p.weightNumber - totalWeight) < 0.01) return p
  }
  return null
}

/**
 * 骨重数值 → 中文字符串（"四两二钱"、"五两一钱"、"三两"）
 *   - 整数部分为"两"，必然存在
 *   - 小数部分为"钱"，为 0 时省略
 *
 * 数字 → 中文映射仅覆盖 0–9，满足称骨两数合法区间 2.1..7.1。
 */
const CHINESE_DIGITS = ['零', '一', '二', '三', '四', '五', '六', '七', '八', '九'] as const

export function formatWeight(totalWeight: number): string {
  // 避开浮点抖动：乘 10 后取整再拆（2.3 -> 23, 3.0 -> 30）
  const scaled = Math.round(totalWeight * 10)
  const liang = Math.floor(scaled / 10)
  const qian = scaled % 10
  const liangPart = `${CHINESE_DIGITS[liang] ?? liang}两`
  const qianPart = qian === 0 ? '' : `${CHINESE_DIGITS[qian]}钱`
  return `${liangPart}${qianPart}`
}
