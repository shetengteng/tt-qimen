export type GodLocale = 'zh-CN' | 'zh-TW' | 'en'

/**
 * 把 tyme4ts 输出的简体吉神/凶神原名翻译为指定 locale 的显示名。
 *
 * @param name tyme4ts 简体名（如 "天牢"）；不在映射表内时按 fallback 处理
 * @param locale 目标显示语言；'zh-CN' 直接返回原名（无需查表）
 * @returns 译名；找不到时回原名（避免 UI 出现 undefined）
 */
export function translateGod(name: string, locale: GodLocale): string {
  if (!name) return name
  if (locale === 'zh-CN') return name
  const dict = locale === 'zh-TW' ? ZH_TW : EN
  return dict[name] ?? name
}

/** 仅诊断 / 测试用：返回未翻译的名称数。 */
export function _untranslatedCount(names: readonly string[], locale: GodLocale): number {
  if (locale === 'zh-CN') return 0
  const dict = locale === 'zh-TW' ? ZH_TW : EN
  return names.filter((n) => !dict[n]).length
}
