/**
 * 取指定格局的长解读（兼容旧签名，返回 interpret 字符串）。
 *
 * @param pattern 识别出的格局名（来自 PatternInfo.name）
 * @returns 200 字左右的现代化长解读
 *
 * 使用示例：
 *   const longText = getPatternLongInterpret(chart.pattern.name)
 *   showPanel(longText)
 */
export function getPatternLongInterpret(pattern: PatternName): string {
  return (
    PATTERN_LONG_INTERPRET[pattern]?.interpret
    ?? '该格局以财官印食的常规路径论之，四柱中和、喜用得宜则吉，刑冲破合失衡则忌。宜以稳健务实为处世原则，顺应本命喜用之气。'
  )
}

/**
 * 取指定格局的完整长解读条目（含 classical + source，供 UI 显示出处）。
 *
 * @param pattern 识别出的格局名（来自 PatternInfo.name）
 * @returns SourcedContent 结构；未命中时返回 null（调用方自行 fallback 到 getPatternLongInterpret）
 *
 * 使用示例：
 *   const entry = getPatternLongEntry(chart.pattern.name)
 *   if (entry) {
 *     showPanel(entry.interpret)
 *     showSource(`—— ${entry.source}`, entry.classical)
 *   }
 */
export function getPatternLongEntry(pattern: PatternName): PatternLongEntry | null {
  return PATTERN_LONG_INTERPRET[pattern] ?? null
}
