/** "扶身组"：印 + 比劫 */
const SUPPORT_TEN_GODS: TenGodType[] = ['比肩', '劫财', '正印', '偏印']

/**
 * 判定大运十神的取向（顺势/逆势）
 *
 * @param tenGod 大运天干十神
 * @param strength 日主旺衰
 * @returns
 *   - follow（顺势）：十神有利于当前日主旺衰
 *   - oppose（逆势）：十神不利于当前日主旺衰
 *
 * 规则：
 *   - 中和（中庸取吉）：默认顺势
 *   - 身强（偏旺/极旺）：逢克泄组（食伤财官杀）→ 顺势；逢扶身组（印比）→ 逆势
 *   - 身弱（偏弱/极弱）：逢扶身组（印比）→ 顺势；逢克泄组（食伤财官杀）→ 逆势
 *   - 日主入运（不应在大运中出现）：默认顺势作为兜底
 */
export function judgeDecadeStance(tenGod: TenGodType, strength: StrengthLevel): DecadeStance {
  if (tenGod === '日主') return 'follow'
  if (strength === '中和') return 'follow'
  const isSupport = SUPPORT_TEN_GODS.includes(tenGod)
  const isStrong = strength === '偏旺' || strength === '极旺'
  if (isStrong) return isSupport ? 'oppose' : 'follow'
  return isSupport ? 'follow' : 'oppose'
}

/**
 * v2 主表 lookup + 变体抽取
 *
 * @param tenGod 大运天干十神
 * @param strength 日主旺衰
 * @param stance 取向（顺势/逆势）
 * @param variantIdx 变体索引（一般传 decade 序号 0-7，内部 mod 3）
 * @returns 命中 v2 → { hint: string, tags: string[] }；未命中 → null（builder 应回退到 v1）
 */
export function getDecadeHintV2(
  tenGod: TenGodType,
  strength: StrengthLevel,
  stance: DecadeStance,
  variantIdx: number,
): { hint: string, tags: string[] } | null {
  const key = `${tenGod}_${strength}_${stance}` as DecadeHintV2Key
  const entry = DECADE_HINTS_V2[key]
  if (!entry) return null
  const idx = ((variantIdx % 3) + 3) % 3
  return { hint: entry.variants[idx], tags: entry.tags }
}

